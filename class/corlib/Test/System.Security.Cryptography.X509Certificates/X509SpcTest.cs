		[Test]
		public void InvalidSignature () 
		{
			FileStream fs = new FileStream ("smallspc.exe", FileMode.Create, FileAccess.Write);
			fs.Write (smallspcexe, 0, smallspcexe.Length);
			fs.Position = smallspcexe.Length >> 1;
			byte[] mark = Encoding.ASCII.GetBytes ("bad signature");
			fs.Write (mark, 0, mark.Length);
			fs.Close ();

			try {
				X509Certificate spc = X509Certificate.CreateFromSignedFile ("smallspc.exe");
			}
			catch (COMException ce) {
				if (ce.ErrorCode != -2146762496) // 0x800b0100
					throw; 
				// this is expected
				// The digital signature of the object did not verify
			}
			catch (Exception e) {
				Fail ("Expected COMException but got " + e.ToString ());
			}
		}

