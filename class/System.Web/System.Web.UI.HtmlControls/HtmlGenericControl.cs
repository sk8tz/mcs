	using System;
	using System.Web;
	using System.Web.UI;
	
	namespace System.Web.UI.HtmlControls{
		
		public class HtmlGenericControl : HtmlContainerControl {
			
			public HtmlGenericControl(){}
			
			public HtmlGenericControl(string tag): base(tag){}
			
			public new string TagName {
				get
				{
					return _tagName;
				}
				set{
					_tagName = value;
				}
			}
		}
	}
